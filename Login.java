    private void iniciarSesionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_iniciarSesionMouseClicked
        String usuario = usuarioTxt.getText();
        String password = String.valueOf(passwordTxt.getPassword());
        System.out.println(usuario);
        System.out.println(password);
        if(usuario.equals("Admin")&& password.equals("root")&& idioma==3){
        new Admin().setVisible(true);
        dispose();
        }
        else if(usuario.equals("Admin")&& password.equals("root")&& idioma==2){
        new AdminEN().setVisible(true);
        dispose();
        }
        else if(usuario.equals("Admin")&& password.equals("root")&& idioma==1){
        new AdminDE().setVisible(true);
        dispose();
        }
        else{
            if(idioma==3){
              javax.swing.JOptionPane.showMessageDialog(this, "Acceso denegado");
            }
            else if (idioma==2){
                javax.swing.JOptionPane.showMessageDialog(this, "Access denied");
            }
            else if (idioma==1){
                javax.swing.JOptionPane.showMessageDialog(this, "Zugriff abgelehnt");
            }
        }
    }//GEN-LAST:event_iniciarSesionMouseClicked

