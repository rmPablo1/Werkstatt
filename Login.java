    private void usuarioTxtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_usuarioTxtMouseClicked
        String password = String.valueOf(passwordTxt.getPassword());
        usuarioTxt.setBackground(Color.white);
        usuarioTxt.setForeground(Color.black);
        passwordTxt.setBackground(Color.gray);
        if (usuarioTxt.getText().equals("Escribe tu nombre de usuario")) {
            usuarioTxt.setText("");
        }
        if (password.equals("")) {
            passwordTxt.setText("*****");
        }

    }//GEN-LAST:event_usuarioTxtMouseClicked

    private void passwordTxtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_passwordTxtMouseClicked
        usuarioTxt.setBackground(Color.gray);
        passwordTxt.setBackground(Color.white);
        passwordTxt.setForeground(Color.black);
        if (String.valueOf(passwordTxt.getPassword()).equals("*****")) {
            passwordTxt.setText("");
        }
        if (usuarioTxt.getText().equals("")) {
            usuarioTxt.setText("Escribe tu nombre de usuario");
        }
    }//GEN-LAST:event_passwordTxtMouseClicked

